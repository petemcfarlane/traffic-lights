<?php

namespace TrafficLights;

class AsciiRenderer implements Renderer
{
    public function render(StateMachine $state)
    {
        switch ($state) {
            case 'stop':
                return '
.------------.
|    .==.    |
|   /RRRR\   |
|   \RRRR/   |
|    `==`    |
|    .==.    |
|   /    \   |
|   \    /   |
|    `==`    |
|    .==.    |
|   /    \   |
|   \    /   |
|    `==`    |
|--._.--._.--|
    [____]
     [##]
';

            case 'prepare to go':
                return '
.------------.
|    .==.    |
|   /RRRR\   |
|   \RRRR/   |
|    `==`    |
|    .==.    |
|   /::::\   |
|   \::::/   |
|    `==`    |
|    .==.    |
|   /    \   |
|   \    /   |
|    `==`    |
|--._.--._.--|
    [____]
     [##]
';
            case 'go':
                return '
.------------.
|    .==.    |
|   /    \   |
|   \    /   |
|    `==`    |
|    .==.    |
|   /    \   |
|   \    /   |
|    `==`    |
|    .==.    |
|   /&&&&\   |
|   \&&&&/   |
|    `==`    |
|--._.--._.--|
    [____]
     [##]
';
            case 'prepare to stop':
                return '
.------------.
|    .==.    |
|   /    \   |
|   \    /   |
|    `==`    |
|    .==.    |
|   /::::\   |
|   \::::/   |
|    `==`    |
|    .==.    |
|   /    \   |
|   \    /   |
|    `==`    |
|--._.--._.--|
    [____]
     [##]
';
        }
    }
}
